import { readFileSync, writeFileSync } from 'node:fs';
import { createRequire } from 'node:module';
import { dirname, relative, resolve } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

import { currentPackages, roadmapPackages } from './package-catalog.data.mjs';

const scriptDirectory = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(scriptDirectory, '..');
const angularRequire = createRequire(resolve(repoRoot, 'angular/package.json'));
const prettierModule = await import(pathToFileURL(angularRequire.resolve('prettier')).href);
const prettierApi = prettierModule.default ?? prettierModule;
const { format, resolveConfig } = prettierApi;
const generatedCatalogPath = resolve(
  repoRoot,
  'angular/apps/demo-angular/src/app/generated/package-catalog.ts',
);
const packageCatalogDocsPath = resolve(repoRoot, 'docs/packages/README.md');
const demoRunbookPath = 'docs/demo/README.md';
const gitHubBlobBase = 'https://github.com/HexGuard/hexguard/blob/main';
const gitHubTreeBase = 'https://github.com/HexGuard/hexguard/tree/main';
const matrixHeading = '## Broader Prioritization Matrix';

async function formatWithResolvedConfig(source, filepath) {
  const resolvedConfig =
    (await resolveConfig(filepath, {
      editorconfig: true,
    })) ?? {};

  return format(source, {
    ...resolvedConfig,
    filepath,
  });
}

function toGitHubBlob(path) {
  return `${gitHubBlobBase}/${path}`;
}

function toGitHubTree(path) {
  return `${gitHubTreeBase}/${path}`;
}

function toDocsRelativePath(targetPath) {
  return relative(dirname(packageCatalogDocsPath), resolve(repoRoot, targetPath)).replace(
    /\\/g,
    '/',
  );
}

function createDocsLinks(pkg) {
  return [
    {
      label: 'Package README',
      href: toGitHubBlob(pkg.readmePath),
    },
    {
      label: 'Deep package notes',
      href: toGitHubBlob(pkg.deepDivePath),
    },
    {
      label: 'Demo runbook',
      href: toGitHubBlob(demoRunbookPath),
    },
  ];
}

function createGeneratedCurrentPackages() {
  return currentPackages.map((pkg) => ({
    id: pkg.id,
    packageName: pkg.packageName,
    status: pkg.status,
    scope: pkg.scope,
    summary: pkg.summary,
    detail: pkg.detail,
    installCommand: pkg.installCommand,
    featureHighlights: pkg.featureHighlights,
    bestFitScenarios: pkg.bestFitScenarios,
    statusNoteParagraphs: pkg.statusNoteParagraphs,
    repositoryHref: toGitHubTree(pkg.repositoryPath),
    docsLinks: createDocsLinks(pkg),
  }));
}

function createGeneratedRoadmapPackages() {
  return roadmapPackages.map((pkg) => ({
    id: pkg.id,
    anchorId: pkg.anchorId,
    packageName: pkg.packageName,
    scope: pkg.scope,
    status: pkg.status,
    summary: pkg.summary,
    docsHref: `${toGitHubBlob('docs/packages/README.md')}#${pkg.anchorId}`,
    showOnSiteHome: pkg.showOnSiteHome,
  }));
}

function renderGeneratedCatalogSource() {
  const generatedCurrentPackages = createGeneratedCurrentPackages();
  const generatedRoadmapPackages = createGeneratedRoadmapPackages();

  return `// This file is generated by scripts/sync-package-catalog.mjs.

export interface GeneratedCatalogLink {
  readonly label: string;
  readonly href: string;
}

export interface GeneratedCurrentPackageCatalogEntry {
  readonly id: string;
  readonly packageName: string;
  readonly status: 'Available' | 'In Progress';
  readonly scope: 'Angular' | '.NET';
  readonly summary: string;
  readonly detail: string;
  readonly installCommand: string;
  readonly featureHighlights: readonly string[];
  readonly bestFitScenarios: readonly string[];
  readonly statusNoteParagraphs: readonly string[];
  readonly repositoryHref: string;
  readonly docsLinks: readonly GeneratedCatalogLink[];
}

export interface GeneratedRoadmapPackageEntry {
  readonly id: string;
  readonly anchorId: string;
  readonly packageName: string;
  readonly scope: 'Angular' | '.NET' | 'Cross-stack';
  readonly status: 'Planned' | 'Proposed';
  readonly summary: string;
  readonly docsHref: string;
  readonly showOnSiteHome: boolean;
}

export const GENERATED_CURRENT_PACKAGES = ${JSON.stringify(generatedCurrentPackages, null, 2)} as const satisfies readonly GeneratedCurrentPackageCatalogEntry[];

export const GENERATED_ROADMAP_PACKAGES = ${JSON.stringify(generatedRoadmapPackages, null, 2)} as const satisfies readonly GeneratedRoadmapPackageEntry[];

export function getGeneratedCurrentPackage(packageId: string): GeneratedCurrentPackageCatalogEntry {
  const packageEntry = GENERATED_CURRENT_PACKAGES.find((entry) => entry.id === packageId);

  if (!packageEntry) {
    throw new Error(\`Missing generated current package entry for \${packageId}.\`);
  }

  return packageEntry;
}
`;
}

function renderCurrentPackagesTable() {
  const header = [
    '| Package | Status | Description | Primary Docs |',
    '| ------- | ------ | ----------- | ------------ |',
  ];
  const rows = currentPackages.map((pkg) => {
    const deepDiveLink = `[Deep Dive](${toDocsRelativePath(pkg.deepDivePath)})`;
    const readmeLink = `[npm-facing README](${toDocsRelativePath(pkg.readmePath)})`;
    const demoRunbookLink = `[Demo runbook](${toDocsRelativePath(demoRunbookPath)})`;

    return `| \`${pkg.packageName}\` | ${pkg.status} | ${pkg.summary} | ${deepDiveLink}, ${readmeLink}, ${demoRunbookLink} |`;
  });

  return [...header, ...rows].join('\n');
}

function renderCurrentPackageNotes() {
  return currentPackages
    .map(
      (pkg) => `
<a id="package-${pkg.id}"></a>

### \`${pkg.packageName}\`

Status: ${pkg.status}

${pkg.statusNoteParagraphs.join('\n\n')}`,
    )
    .join('\n');
}

function renderRoadmapBriefs() {
  return roadmapPackages
    .map(
      (pkg) => `
<a id="${pkg.anchorId}"></a>

### \`${pkg.packageName}\`

Status: ${pkg.status}

${pkg.summary}`,
    )
    .join('\n');
}

function renderPackageCatalogDocs(existingDocsSource) {
  const matrixIndex = existingDocsSource.indexOf(matrixHeading);

  if (matrixIndex === -1) {
    throw new Error(`Could not find \`${matrixHeading}\` in docs/packages/README.md.`);
  }

  const suffix = existingDocsSource.slice(matrixIndex).trimStart();

  return `# Package Catalog

This page tracks the published package, the next planned packages, and how they relate to one
another.

<!-- The sections above the prioritization matrices are generated by scripts/sync-package-catalog.mjs. -->

## Current Packages

${renderCurrentPackagesTable()}

## Package Status Notes
${renderCurrentPackageNotes()}

## Planned and Proposed Package Briefs
${renderRoadmapBriefs()}

${suffix}
`;
}

const existingDocsSource = readFileSync(packageCatalogDocsPath, 'utf8');

writeFileSync(
  generatedCatalogPath,
  await formatWithResolvedConfig(renderGeneratedCatalogSource(), generatedCatalogPath),
);

writeFileSync(
  packageCatalogDocsPath,
  await formatWithResolvedConfig(
    renderPackageCatalogDocs(existingDocsSource),
    packageCatalogDocsPath,
  ),
);
